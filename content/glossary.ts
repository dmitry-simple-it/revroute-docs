export type GlossaryBlock =
  | { type: 'p'; text: string }
  | { type: 'h2'; text: string }
  | { type: 'h3'; text: string }
  | { type: 'ul'; items: string[] }
  | { type: 'ol'; items: string[] }
  | { type: 'table'; headers: string[]; rows: string[][]; caption?: string }
  | { type: 'attribution'; models: { name: string; weights: number[] }[]; touchpoints: string[]; note?: string }

export type GlossaryEntry = {
  slug: string
  /** Короткое название (используется как H1 и в title) */
  title: string
  /** Заголовок в meta-title, если нужно длиннее или с уточнением */
  metaTitle?: string
  /** Однопредложенческое определение для description, list-карточки и DefinedTerm.schema */
  definition: string
  /** Категория для группировки в списке (необязательно) */
  category: string
  /** Тело статьи */
  content: GlossaryBlock[]
  /** FAQ внизу страницы */
  faq?: { q: string; a: string }[]
  /** Ссылки на связанные термины из этого же словаря */
  relatedTerms?: string[]
  /** Ссылки на маркетинговые страницы продукта */
  relatedPages?: { href: string; label: string; desc: string }[]
  /** Источники для проверяемых цифр и утверждений в тексте */
  sources?: { label: string; url: string; note?: string }[]
  /** Контекстный CTA внизу страницы. Если не задан — используется fallback про /partners */
  cta?: {
    title: string
    desc: string
    primary: { href: string; label: string }
    secondary?: { href: string; label: string }
  }
}

